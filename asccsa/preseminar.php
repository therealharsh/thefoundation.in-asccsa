				<p>
				<b>Let’s #TalkSexuality: The Whys and Hows of an Affirmative Approach to Addressing Sexuality<br>
				Facilitator: TARSHI (Tanisha Chaddha and Dipika Srivastava)<br>
				No. of Days: 1<br>
				Maximum no. of participants: 25<br>
				Suitable participants: Stakeholders working in the field, trainers, teachers</b><br>

				<b>About the workshop:</b><br>
				Children and young people growing up with accurate and matter-of-fact information and messages 
				on sexuality, are likely to have better emotional and mental health and well-being. And, people who 
				have a healthy sense of themselves make well-informed and better decisions that do people who 
				think poorly of themselves. Sexuality is often a little-understood and highly controversial topic, used 
				interchangeably with sexual intercourse or sexual orientation, when in fact it is a much broader idea. 
				Understanding sexuality involves identifying and exploring a wide range of issues, emotions, 
				experiences and topics included under its rubric. Similarly, an understanding of gender requires 
				appreciating the valuable potential of acknowledging that it is socially constructed. TARSHI’s 
				workshop will address the following:<br>
				<b>1.</b> Expanding the understanding of sex and sexuality and gender<br>
				<b>2.</b> Exploring ways of equipping oneself to be able to give affirming messages and information around 
				sexuality on children and young people, for their safety and well-being<br>

				<b>About the facilitator:</b><br>
 				TARSHI (Talking About Reproductive and Sexual Health Issues) is a registered NGO based in New 
				Delhi, India founded in 1996 that works to TARSHI support and enable people's control and 
				agency over their sexual and reproductive health and well-being through information 
				dissemination, knowledge and perspective building, within a human rights framework. TARSHI 
				believes that all people have the right to sexual well-being and to a self-affirming and enjoyable 
				sexuality and aims to address all people, especially women and young people through its various 
				programmes. For more information, please visit <a href="http://www.tarshi.net">www.tarshi.net.</a></p>

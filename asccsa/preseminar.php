						<b>Using the Traumagenic Dynamic Framework to assess pre- and adolescent girls who have been sexually abused<br> 
						Facilitator: Dr. Lois Engelbrecht<br>
						No. of days: 2<br>
						Maximum no. of participants: 25<br>
						Suitable participants: Mental health professionals, therapists, and social workers</b><br>
						<b>About the workshop:</b><br>
						Against the context of the ecological model, socio-cultural realities and from a mental health and
						therapeutic perspective, the workshop aims to give participants an overview of the concerns  and
						variables involved, to address  effects of sexual abuse on the well-being of a  child who has been
						sexually abused  and their family. Discussion would be around case study reviews and examples of
						activities related to case presentations.<br>

						<b>About the facilitator:</b><br>
						Dr Lois Engelbrecht was born and brought up in India. After studying in the United States, she moved 
						to Asia in 1985 and worked as a freelance social worker focusing on child sexual abuse and 
						community organization. She founded the Center for the Prevention and Treatment of Child Sexual 
						Abuse in Manila in 1995 and still sits on its Board of Trustees. She now lives in Ghana, but continues 
						to develop training programmes and materials. Several Asian and Middle Eastern cities have invited 
						her to help replicate the CPTCSA.
					</p>	

					<b>It starts with us – Evolving a Child Protection Policy<br>
					Facilitator: Vidya Reddy, TULIR<br>
					No. of Days: 3 hours<br>
					Maximum no. of participants: 50<br>
					Suitable participants: Parents, teachers, youth serving organizations, stakeholders</b><br>
					
					<b>About the workshop:</b><br>
					A child protection policy (CPP) articulates an organization zero tolerance approach to child abuse. An 
					important step towards achieving this objective would be for every institution which works with and 
					for children to develop and adhere to a child protection policy which would have clear cut guidelines 
					and protocols to provide for a safe and conducive environment for both children and the adults with 
					whom they interact. The goal of this workshop is to assist participants with an understanding of 
					evolving  a policy suited to their uniqueness but within a framework of non negotiables, for reducing 
					and managing risks of child abuse by persons engaged in delivering the organization program 
					activities.<br>

					<b>About the facilitator:</b><br>
					Tulir - Centre for the Prevention and Healing of Child Sexual Abuse is a non-profit, non-governmental 
					organization committed to working against child sexual abuse in India, and is involved with both 
					direct intervention and advocacy activities at various levels across the country and South Asia region. 
					Tulir’s mandate of addressing child sexual abuse besides public education includes child focused 
					programs, creation and strengthening of support networks especially for legal assistance and 
					psychosocial healing of abused children and their families, and developing information resources. 
					Technical assistance and training related to professional response sector involvement are also 
					significant activities besides fostering coalitions and networks to influence policy and legislative
					initiatives. The strategies which underpins Tulir’s programming are designed for broad scale 	
					changes, not only of individuals but also of systems and norms.	</p>

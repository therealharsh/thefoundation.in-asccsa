				<div id="para3">
				<p><b>Stewards of Children Facilitator Training<br>
				Facilitator: Darkness to light (Cindy McElhinney and Paula Sellars)<br>
				No. of Days: 1<br>
				Maximum no. of participants: 15<br>
				Suitable participants: Mental health professionals, therapists, and social workers</b> <br>

				<b>About the workshop:</b><br>
				Stewards of Children is an evidence-informed training program that teaches adults how to prevent, 
				recognize, and react responsibly to child sexual abuse. The program is designed for parents, youth	
				serving organizations, and other concerned individuals. Stewards of Children uses real people and 
				real stories to show you how to protect children. The presentation integrates commentary from 
				sexual abuse survivors, experts in the field, and other concerned adults, all providing practical 
				guidance for preventing and responding to child sexual abuse. Stewards of Children increases 
				knowledge, improves attitudes, and changes child protective behaviors. It offers practical prevention 
				training with a conversational, real-world approach. 
				The goal is to train a core group of 15 Stewards of Children Facilitators who will comprise the Core 
				Advisory Group as D2L adapts Stewards of Children for India. <br>

				<b>About the facilitator:</b><br>
				Darkness to Light (D2L) is a nonprofit organization founded in 2000 with the mission to empower 
				people to prevent child sexual abuse. Their work is guided by a vision to create a world for children 
				where child sexual abuse does not exist. They believe protecting children is an adult responsibility 
				and that education is the first critical step, as well as the catalyst for larger cultural change. Using an 
				evidence-informed platform, D2L programs teach adults to prevent, recognize, and react responsibly 
				to child sexual abuse. D2L works to end abuse by educating, raising awareness, sharing resources, 
				and fostering a network of committed prevention advocates. Till date, they have trained over 8,000 
				Authorized Facilitators who teach the program in 50 states and 16 international locations, and more 
				than 980,000 copies of Stewards of Children have been distributed to communities around the 
				world.</p>

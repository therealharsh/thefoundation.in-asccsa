				<div id="para6">
				<p><b>Working with Sexually Abusive Children and Community Systems<br>
				Facilitator: Ms. Zenaida Rosales<br>
				No. of Days: 1<br>
				Maximum no. of participants: 30<br>
				Suitable participants: Mental health professionals, therapists, and social workers</b><br>
				
				<b>About the workshop:</b><br>
				The causes of sexual abusive behaviors are varied and include both victimization experience and 
				learning from family and community behaviors and culture. Regardless of the cause, however, 
				children abusing children is a reality today. If the public and communities remain reluctant to 
				acknowledge the existence of children with abusive sexual behaviors, treatment options to stop the 
				sexual offending of these children at early ages will also remain uncertain. The aim of this workshop 
				is to look at characteristics of the 40 young sexual offenders who have come to CPTCSA for 
				treatment. With this information, we can design and implement effective services for prevention 
				and treatment that focus on the individual, family, community and culture.<br>
				
				<b>About the facilitator:</b><br>
				Ms. Zenaida Rosales is the Co-Founder and Executive Director of Center for the Prevention and 
				Treatment of Child Sexual Abuse (CPTCSA).  She is a social worker with years of experience of 
				providing therapy to victims and young. She has been working with the government as Vice 
				Chairperson- Special Committee against Child Sexual Abuse and Commercial Sexual Exploitation of 
				Children (SACSEC) of the Council for the Protection of -Children (CWC) and NGO representative to 
				the Juvenile Justice Welfare Council in putting together laws and networks. She has also been 
				consultant/mentor- Plan International Philippines- Group Counseling Program for Sexually Abused 
				Children as well as spoke on child protection in Japan, Thailand, Vietnam, China, and Cambodia. For 
				more information, please visit <a href="http://www.cptcsa.org">www.cptcsa.org.</a></p>

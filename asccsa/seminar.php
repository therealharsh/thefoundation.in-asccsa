			<b>Paula Sellars, Author and Director of Stewards of Children, Darkness to Light</b><br>

			Ms. Sellars is an expert in culture-changing educational program design. She is a former child 

			and family therapist, and expert in CSA and trauma recovery.

			Darkness to Light (D2L) is a nonprofit organization founded in 2000 with the mission to 

			empower people to prevent child sexual abuse. Their work is guided by a vision to create a 

			world for children where child sexual abuse does not exist. They believe protecting children is 

			an adult responsibility and that education is the first critical step, as well as the catalyst for 

			larger cultural change. Using an evidence-informed platform, D2L programs teach adults to 

			prevent, recognize, and react responsibly to child sexual abuse. D2L works to end abuse by 

			educating, raising awareness, sharing resources, and fostering a network of committed 

			prevention advocates. Till date, they have trained over 8,000 Authorized Facilitators who teach 

			the program in 50 states and 16 international locations, and more than 980,000 copies of 

			Stewards of Children have been distributed to communities around the world.</p>


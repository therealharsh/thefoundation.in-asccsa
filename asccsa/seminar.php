			<div id="cehat"><p>

			<b>CEHAT</b>

			The idea of CEHAT was conceived 20 years ago when a group of researchers and healthcare 

			professionals decided to create an alternative health research institution which is at the 

			interface of activism and academics. CEHAT comprises of a multi-disciplinary team such as 

			doctors, lawyers, social workers, public health experts and counsellors. CEHAT through its 

			research, intervention, education and advocacy, has been addressing issues of right to health 

			care for all; investigating and combating violence; and caring for survivors. CEHAT has 

			pioneered a comprehensive healthcare model in 3 public hospitals in Mumbai, based on 

			standards set by the World Health Organization for sexual assault survivors. Since 2008, the 

			services have extended to over 250 women and children.</p>


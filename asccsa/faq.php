		     			<b>Where and when will the ASCCSA 2015 take place?</b><br>
ASCCSA 2015 will take place at The Godrej Dance Theatre, NCPA, Nariman Point, Mumbai on 22nd 
and 23rd September 2015. The pre-seminar workshops will be held on 20th and 21st September 
2015 at YMCA International Guesthouse, Mumbai Central, Mumbai.<br><br>

<b>How can I attend the ASCCSA 2015?</b><br>
Please fill in the registration form on the registration page. Once you have submitted your 
conference registration form, a confirmation note will be sent.<br><br>

<b>Do I have to pay for the seminar?</b><br>
No, the seminar is free to attend. However, the pre-seminar workshops are charged.<br><br>

<b>Can I register for the conference online?</b><br>
Yes, online registrations are accepted.Please go to the “Registration" tab. If you have questions, 
please contact the team at projectheal.thefoundation@gmail.com or call 022 23521641.<br><br>

<b>How can I pay the pre-seminar workshop fees?</b><br>
You can pay the workshop fee via cheque / bank draft/online transfer. Your attendance will not be 
confirmed until payment is received at the organiser’s end.<br><br>

<b>Do I need to select which workshops that I want to attend prior to the conference?</b><br>
Yes, attendees are required to select their workshops prior to the conference. All registrations are 
on a first-come, first-served basis.<br><br>

<b>If I sign up now and cannot attend the conference, can you refund the fees paid?</b><br>
Request for refunds should be made in writing before 1st September 2015. Refunds will be subjected 
to an administrative charge to be borne by the registrant. Bank charges that occur will also have to 
be borne by the registrant. <br><br>

<b>Can I change the sessions I registered for?</b><br>
Yes, sessions can be changed subject to availability. However any difference in fees between the 
sessions originally registered for and replacement sessions will not be refunded. Any shortfall in fees 
must be paid for before attendance is confirmed.<br><br>

<b>How many people from one organization can attend each workshop?</b><br>
Only a limited number of people per organization are allowed for each workshop. Please check the 
pre-seminar workshop page for details. You can contact the team at 
projectheal.thefoundation@gmail.com for any questions.<br><br>

<b>I just sent the registration form to you. Can you send me a confirmation immediately?</b><br>
The organiser will require approximately 3 working days to acknowledge your registration. 
Confirmations for pre-seminar workshops will be sent later<br><br>

<b>What meals are provided at the seminar?</b><br>
Lunch will be provided on all days (seminar and pre-seminar). Tea will be served in the morning and 
at the end of the day.<br><br>

<b>Will you be making my travel and accommodation arrangements?</b><br>
Attendees will have to make their own travel and stay arrangements. Our team will be happy to 
assist you in planning for travel and stay close to the venue. However, the costs will be borne by the 
participants. Please check our travel page for options.
